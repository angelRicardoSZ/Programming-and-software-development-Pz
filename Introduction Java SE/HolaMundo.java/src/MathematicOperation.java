public class MathematicOperation {
    public static void main(String[] args) {
        double x = 2.1;
        double y = 3;
        System.out.println(Math.PI);
        System.out.println(Math.E);
        System.out.println(Math.ceil(x)); // entero hacia arriba
        System.out.println(Math.floor(x)); // entero hacia abajo

        System.out.println(Math.pow(x,y)); // x elevado a y
        System.out.println(Math.max(x,y)); // devuelve el numero mayor
        System.out.println(Math.sqrt(y));  // devuelve la raiz de y
        System.out.println(Math.PI * Math.pow(y,2)); // area circulo

    }
}
